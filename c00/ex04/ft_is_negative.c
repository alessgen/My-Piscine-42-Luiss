#include <unistd.h>

void	ft_is_negative(int n)
{
	char	d;
	char	e;

	d = 'N';
	e = 'P';
	if (n >= 0)
	{
		write(1, &e, 1);
	}
	if (n < 0)
	{
		write(1, &d, 1);
	}
}
