#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	b;

	b = 0;
	while (str[b] != '\0')
	{
		if (str[b] < '0' || str[b] > '9')
		{
			return (0);
		}
		b++;
	}
	return (1);
}
