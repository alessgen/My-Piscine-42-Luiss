#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int	b;

	b = 0;
	while (str[b] != '\0')
	{
		if (str[b] < 'A' || str[b] > 'Z')
		{
			return (0);
		}
		b++;
	}
	return (1);
}
